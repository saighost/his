package cn.honry.inner.vo;

public class KeyValueVo {
	
	/**键**/
	private String key;
	/**值**/
	private String val;
	
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public String getVal() {
		return val;
	}
	public void setVal(String val) {
		this.val = val;
	}
	
	
}
